#include "colors.h"

Colors::Colors()
{

}

QColor Colors::khakiWeb()
{
    // Khaki web
    return QColor::fromRgb(183, 173, 153);
}

QColor Colors::radicalRed()
{
    // Radical red
    return QColor::fromRgb(255, 67, 101);
}

QColor Colors::richBlack()
{
    // Rich black
    return QColor::fromRgb(3, 3, 1);
}

QColor Colors::turquise()
{
    // Turquise
    return QColor::fromRgb(0, 217, 192);
}

QColor Colors::ivory()
{
    // Ivory
    return QColor::fromRgb(255, 255, 243);
}
